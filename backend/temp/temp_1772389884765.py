# String Operations
s = "Hello, World!"
print(f"Length: {len(s)}")
print(f"Upper: {s.upper()}")
print(f"Lower: {s.lower()}")
print(f"Slice [0:5]: {s[0:5]}")
print(f"Reverse: {s[::-1]}")
print(f"Replace: {s.replace('World', 'Python')}")
print(f"Split: {s.split(', ')}")
print(f"Find 'World': {s.find('World')}")